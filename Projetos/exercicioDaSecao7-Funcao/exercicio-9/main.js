//Escreva uma função que receba dois números, o primeiro é a base e o segundo a  potência

//depois faça essa operação e retone o resultado 
// por exemplo 3,2=9


function numeros(a,b,){
    // return Math.pow(a,b);
    return a ** b;
}

console.log(numeros(2,2));
console.log(numeros(2,4));
console.log(numeros(5,5));
